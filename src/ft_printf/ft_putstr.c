#include "ft_printf.h"

int	ft_putstr(char	*str)
{
	int	i;

	i = -1;
	if (!str)
	{
		write(1, "(null)", 6);
		i = 6;
	}
	else
		while (str[++i])
			write (1, &str[i], 1);
	return (i);
}
